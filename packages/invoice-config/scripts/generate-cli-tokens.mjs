import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const repoRoot = path.resolve(__dirname, '../../..');
const jsonPath = path.resolve(__dirname, '../tokens.json');
const outPath = path.resolve(
  repoRoot,
  'apps/cli/internal/cmd/tokens_generated.go'
);

async function main() {
  const raw = await fs.readFile(jsonPath, 'utf8');
  const config = JSON.parse(raw);

  const tokens = ensureNonEmptyArray(config.tokens, 'tokens');
  const tokenNetworkMap = ensureObject(config.tokenNetworkMap, 'tokenNetworkMap');
  const networkCurrencyMap = ensureObject(config.networkCurrencyMap, 'networkCurrencyMap');

  validateTokenMapConsistency(tokens, tokenNetworkMap);
  validateNetworks(tokenNetworkMap, networkCurrencyMap);

  const content = renderGoFile({
    tokens,
    tokenNetworkMap,
    networkCurrencyMap,
  });

  await fs.writeFile(outPath, content, 'utf8');
  console.log(`Generated ${path.relative(repoRoot, outPath)}`);
}

function ensureNonEmptyArray(value, fieldName) {
  if (!Array.isArray(value) || value.length === 0) {
    throw new Error(`${fieldName} must be a non-empty array`);
  }

  if (!value.every((item) => typeof item === 'string' && item.length > 0)) {
    throw new Error(`${fieldName} must contain only non-empty strings`);
  }

  return value;
}

function ensureObject(value, fieldName) {
  if (!value || typeof value !== 'object' || Array.isArray(value)) {
    throw new Error(`${fieldName} must be an object`);
  }
  return value;
}

function validateTokenMapConsistency(tokens, tokenNetworkMap) {
  const mapTokens = Object.keys(tokenNetworkMap);

  const tokensSet = new Set(tokens);
  const mapTokensSet = new Set(mapTokens);

  if (tokensSet.size !== tokens.length) {
    throw new Error('tokens contains duplicates');
  }

  if (mapTokensSet.size !== mapTokens.length) {
    throw new Error('tokenNetworkMap contains duplicate keys');
  }

  for (const token of tokens) {
    if (!mapTokensSet.has(token)) {
      throw new Error(`tokenNetworkMap is missing token key: ${token}`);
    }
  }

  for (const token of mapTokens) {
    if (!tokensSet.has(token)) {
      throw new Error(`tokenNetworkMap has unknown token key: ${token}`);
    }

    const networks = tokenNetworkMap[token];
    if (!Array.isArray(networks) || networks.length === 0) {
      throw new Error(`tokenNetworkMap.${token} must be a non-empty array`);
    }

    if (!networks.every((n) => typeof n === 'string' && n.length > 0)) {
      throw new Error(`tokenNetworkMap.${token} must contain only non-empty strings`);
    }
  }
}

function validateNetworks(tokenNetworkMap, networkCurrencyMap) {
  const networks = new Set(Object.keys(networkCurrencyMap));

  for (const [network, currency] of Object.entries(networkCurrencyMap)) {
    if (typeof currency !== 'string' || currency.length === 0) {
      throw new Error(`networkCurrencyMap.${network} must be a non-empty string`);
    }
  }

  for (const [token, tokenNetworks] of Object.entries(tokenNetworkMap)) {
    for (const network of tokenNetworks) {
      if (!networks.has(network)) {
        throw new Error(`tokenNetworkMap.${token} references unknown network: ${network}`);
      }
    }
  }
}

function renderGoFile({ tokenNetworkMap, networkCurrencyMap }) {
  const tokenEntries = Object.entries(tokenNetworkMap)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([token, networks]) => `\t${goString(token)}: {${networks.map(goString).join(', ')}},`)
    .join('\n');

  const aliases = buildNetworkAliases(networkCurrencyMap);
  const aliasEntries = Object.entries(aliases)
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([alias, canonical]) => `\t${goString(alias)}: ${goString(canonical)},`)
    .join('\n');

  return `// Code generated by packages/invoice-config/scripts/generate-cli-tokens.mjs; DO NOT EDIT.
package cmd

var tokenNetworkMap = map[string][]string{
${tokenEntries}
}

var canonicalNetworkByAlias = map[string]string{
${aliasEntries}
}
`;
}

function buildNetworkAliases(networkCurrencyMap) {
  const aliases = {};

  for (const [network, currency] of Object.entries(networkCurrencyMap)) {
    aliases[network.toLowerCase()] = network;
    aliases[currency.toLowerCase()] = network;
  }

  return aliases;
}

function goString(value) {
  const escaped = String(value)
    .replace(/\\/g, '\\\\')
    .replace(/"/g, '\\"');
  return `"${escaped}"`;
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
});
